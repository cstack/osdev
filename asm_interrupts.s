  ; save the registers
  push    eax
  push    ebx
  push    ecx
  push    edx
  push    esi
  push    edi
  push    esp
  push    ebp

  ; call the C function
  call    interrupt_handler

  ; restore the registers
  pop    ebp
  pop    esp
  pop    edi
  pop    esi
  pop    edx
  pop    ecx
  pop    ebx
  pop    eax

  ; restore the esp
  add     esp, 8

  ; return to the code that got interrupted
  iret

